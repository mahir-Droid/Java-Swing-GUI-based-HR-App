    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        // TODO add your handling code here:
        try{
            stmt = conn.createStatement();

            int cdId = Integer.parseInt(id.getText());
            String cdName = name.getText();
            String cdAddress = address.getText();
            String cdSchool = school.getText();
            String cdSchoolCG = schoolCG.getText();
            String cdUni = university.getText();
            String cdUniCG = uniCG.getText();
            int cdExperience = Integer.parseInt(experience.getText());
            String cdPhone = phone.getText();

            
            double credit = 0;
            double cgS = Double.parseDouble(cdSchoolCG);
            double cgU = Double.parseDouble(cdSchoolCG);
            
            credit += ((3/5)*cgU*100) + ((1/5)*cgS*100);  // Updating Credits for University CG and School CG
            
            //Updating Credits for Experience
            
            if(cdExperience>0 && cdExperience<=1)
                credit+= 50;
            else if(cdExperience>1 && cdExperience<=2)
                credit+= 100;
            else if(cdExperience>2 && cdExperience<=3)
                credit+= 150;
            else if(cdExperience>3 && cdExperience<=4)
                credit+= 200;
            else if(cdExperience>4 && cdExperience<=5)
                credit+= 250;
            else if(cdExperience>5)
                credit+= 300;
            else
                credit+=0;
            
            //Updating Credits for University
            
            if(cdUni.equals("DU") || cdUni.equals("BUET"))
                credit += 400;
            
            else if(cdUni.equals("MIST") || cdUni.equals("AUST") || cdUni.equals("RUET") || cdUni.equals("KUET") || cdUni.equals("CUET"))
                credit += 300;
            else if(cdUni.equals("NSU") || cdUni.equals("BRAC"))
                  credit += 150;
            else if(cdUni.equals("IUB") || cdUni.equals("AIUB"))
                credit += 100;
            else
                credit += 50;
            
            
            credit = (int)(credit*10);
            
            
            

            String sql = "UPDATE CANDIDATES SET cdName = '"+cdName+"', cdAddress = '"+cdAddress+"', cdSchool = '"+cdSchool+"', cdSchoolCG = '"+cdSchoolCG+"' ,cdUni = '"+cdUni+"', cdUniCG = '"+cdUniCG+"', cdExperience = '"+cdExperience+"', cdPhone = '"+cdPhone+"', credits = '"+credit+"' ";
            stmt.executeUpdate(sql);

            JOptionPane.showMessageDialog(null,"Candidate Info Updated.");
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_updateButtonActionPerformed

