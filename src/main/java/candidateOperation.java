    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed
        // TODO add your handling code here:
        try{
            stmt = conn.createStatement();
            int cdID = Integer.parseInt(id.getText());
            
            String sql = "SELECT * FROM CANDIDATES WHERE id = '"+cdID+"'";
            rs = stmt.executeQuery(sql);
            
            if(rs.next()){
                name.setText(rs.getString("cdName"));
                address.setText(rs.getString("cdAddress"));
                school.setText(rs.getString("cdSchool"));
                schoolCG.setText(rs.getString("cdSchoolCG"));
                university.setText(rs.getString("cdUni"));
                uniCG.setText(rs.getString("cdUniCG"));
                experience.setText(rs.getString("cdExperience"));
                phone.setText(rs.getString("cdPhone"));
            }
            else{
                JOptionPane.showMessageDialog(null,"Not Found.");
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_searchButtonActionPerformed

