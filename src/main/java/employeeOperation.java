        // TODO add your handling code here:
        try{
            stmt = conn.createStatement();
            int emplId = Integer.parseInt(id.getText());
            String emplName = name.getText();
            String emplBlood = blood.getText();
            String emplFather = fatherName.getText();
            String emplCity = city.getText();
            String emplPhone = phone.getText();
            String emplDept = department.getText();

            String sql = "UPDATE EMPLOYEE SET emplName = '"+emplName+"' ,emplFatherName = '"+emplFather+"' ,emplBlood = '"+emplBlood+"', emplCity = '"+emplCity+"', emplPhone = '"+emplPhone+"', emplDept = '"+emplDept+"' WHERE id = '"+emplId+"'";
            stmt.executeUpdate(sql);
            JOptionPane.showMessageDialog(null,"Employee Updated.");
        }
