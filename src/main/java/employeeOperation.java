    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed
        // TODO add your handling code here:
        try{
            stmt = conn.createStatement();
            int emplID = Integer.parseInt(id.getText());
            
            String sql = "SELECT * FROM EMPLOYEE WHERE id = '"+emplID+"'";
            rs = stmt.executeQuery(sql);
            
            if(rs.next()){
                name.setText(rs.getString("emplName"));
                fatherName.setText(rs.getString("emplFatherName"));
                city.setText(rs.getString("emplCity"));
                blood.setText(rs.getString("emplBlood"));
                phone.setText(rs.getString("emplPhone"));
                department.setText(rs.getString("emplDept"));
            }
            else{
                JOptionPane.showMessageDialog(null,"Not Found.");
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_searchButtonActionPerformed

